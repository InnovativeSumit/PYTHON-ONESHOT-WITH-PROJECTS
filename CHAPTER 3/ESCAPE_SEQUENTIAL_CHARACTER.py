print("Escape Sequence Characters in Python\n")

print("\\n  : New Line")
print("\\t  : Horizontal Tab")
print("\\r  : Carriage Return")
print("\\b  : Backspace")
print("\\f  : Form Feed")
print("\\v  : Vertical Tab")
print("\\\\  : Backslash")
print("\\'  : Single Quote")
print("\\\"  : Double Quote")
print("\\a  : Bell / Alert")
print("\\N{name} : Unicode Character Database")
print("\\u1234 : 16-bit Unicode")
print("\\U00001234 : 32-bit Unicode")
print("\\xhh : Hex Value")

# Bonus: Showing some escape sequences in action
print("\n--- Examples in Action ---")
print("Hello\\nWorld →")
print("Hello\nWorld")

print("Tab\\tSpace →")
print("Hello\tWorld")

print("Backspace: ABC\\bD →")
print("ABC\bD")

print("Bell sound:\\a  (May or may not work depending on system)")
print("\a")
