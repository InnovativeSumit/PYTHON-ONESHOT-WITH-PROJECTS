s = "  Hello Python  "

print("Original:", s)
print("Upper:", s.upper())
print("Lower:", s.lower())
print("Strip:", s.strip())
print("Find 'o':", s.find("o"))
print("Count 'l':", s.count("l"))
print("Split:", s.split())
print("Replace:", s.replace("Python", "World"))
print("Starts with H:", s.startswith("H"))
print("Ends with n:", s.endswith("n"))
print("Reverse:", s[::-1])
