print("=== Typecasting in Python ===")

# int to float
a = 10
print("int to float:", float(a))

# float to int
b = 12.56
print("float to int:", int(b))

# int to string
c = 50
print("int to string:", str(c))

# string to int (string must contain numbers only)
d = "123"
print("string to int:", int(d))

# string to float
e = "45.67"
print("string to float:", float(e))

# number to boolean
print("int to bool (0):", bool(0))     # False
print("int to bool (5):", bool(1))     # True

# bool to int
print("bool to int (True):", int(True))
print("bool to int (False):", int(False))
