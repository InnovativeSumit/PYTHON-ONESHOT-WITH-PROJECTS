# Integer
age = 21
print("Integer:", age, type(age))

# Float
price = 99.99
print("Float:", price, type(price))

# String
name = "Sumit"
print("String:", name, type(name))

# Boolean
is_active = True
print("Boolean:", is_active, type(is_active))

# List
fruits = ["apple", "banana", "mango"]
print("List:", fruits, type(fruits))

# Tuple (similar to list but cannot change values)
point = (10, 20)
print("Tuple:", point, type(point))

# Set (no duplicates)
unique_numbers = {1, 2, 3, 3, 4}
print("Set:", unique_numbers, type(unique_numbers))

# Dictionary (key-value pairs)
student = {
    "name": "Sumit",
    "age": 21,
    "grade": "A"
}
print("Dictionary:", student, type(student))
