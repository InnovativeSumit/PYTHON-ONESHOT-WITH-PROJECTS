def variable_rules():
    print("=== Python Variable Naming Rules ===")
    print("1. A variable name must start with a letter or underscore (_)")
    print("2. A variable name cannot start with a number")
    print("3. A variable name can only contain letters, numbers, and underscores")
    print("4. Variable names are case-sensitive (age, Age, AGE are different)")
    print("5. No spaces allowed in variable names")
    print("6. You cannot use Python keywords as variable names (like 'if', 'while', 'class')")
    print("7. Variable names should be meaningful")

variable_rules()
