# Taking two numbers
a = 10
b = 3

print("a =", a)
print("b =", b)

print("\n=== Arithmetic Operations ===")

# Addition
print("Addition (a + b):", a + b)

# Subtraction
print("Subtraction (a - b):", a - b)

# Multiplication
print("Multiplication (a * b):", a * b)

# Division
print("Division (a / b):", a / b)

# Floor Division
print("Floor Division (a // b):", a // b)

# Modulus (Remainder)
print("Modulus (a % b):", a % b)

# Exponent (Power)
print("Exponent (a ** b):", a ** b) # (a^b) <== not valid


a = 10
b = 20

print("a =", a)
print("b =", b)

print("\n=== Comparison & Equality Operators ===")

# Greater than
print("a > b :", a > b)

# Less than
print("a < b :", a < b)

# Greater than or equal to
print("a >= b :", a >= b)

# Less than or equal to
print("a <= b :", a <= b)

# Equal to
print("a == b :", a == b)

# Not equal to
print("a != b :", a != b)


